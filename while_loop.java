public class while_loop {
    public static void main(String[] args) {
        int i = 0;

        //While loop
        while(i < 5){
            System.out.println("Hello");
            i++;
        }

        //do while loop

        int k = 0;
        do{
            System.out.println("hello Do while loop");
            k++;
        }
        while(k < 5);
    }
    
}